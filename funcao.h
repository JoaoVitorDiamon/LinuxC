#include <stdio.h>
#include <stdlib.h>

float Soma(float soma1, float soma2){
	float TotalSoma;

	TotalSoma = soma1 + soma2;

	return TotalSoma;
}

float Subtracao (float sub1, float sub2){
	float TotalSub;

	TotalSub = sub1 - sub2;

	return TotalSub;
}

float Divisao (float div1, float div2){
	float TotalDiv;

	TotalDiv = div1 / div2;

	return TotalDiv;
}

float Multiplicacao (float mul1,float mul2){
	float TotalMult;

	TotalMult = mul1 * mul2;

	return TotalMult;
}
